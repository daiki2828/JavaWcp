package practice;

public class NoCapsuleMan {
	public String name;
    public int age;

    public NoCapsuleMan(String name, int age) {
        this.name = name;
        this.age = age;
    }
    
    public int getAge() {
    	return age;
    }
    
    public void setAge(int age) {
    	this.age = age;
    }
    
    public String getName() {
    	return name;
    }

}
